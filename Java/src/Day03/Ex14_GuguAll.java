package Day03;

import java.util.Iterator;

public class Ex14_GuguAll {
	public static void main(String[] args) {
		for (int A = 1; A <=9; A++) {
			for (int B = 1; B <=9 ; B++) {
				int result = A*B;
				System.out.print(A + "x" + B + "=" + result);
				System.out.print("\t"); //탭(공백)
				//(단)x(1~9)
			
		}
			//단에 대한 반복 : 1~9
			//각 단의 곱 : 1~9
			
			//중첩 반복문 (이중 반보문)
			//A X B 
			
		System.out.println();
		}
		
	}
}
